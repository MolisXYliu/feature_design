const ANALYZER_SYSTEM_PROMPT = `You are an expert at analyzing AI agent execution traces and suggesting reusable skills.

Your job:
1. Read the execution traces provided (JSON format)
2. Identify patterns that indicate a reusable skill would help:
   - High tool-call count (agent had to "figure things out" step by step)
   - Multiple retries or corrections
   - Domain-specific knowledge the agent had to infer
   - Repeated tool sequences that form a pattern
3. For each pattern, propose ONE skill in the exact format below.

Output ONLY a JSON array of skill proposals. Each proposal must have:
{
  "action": "create" | "patch",
  "skillId": "snake_case_identifier",
  "name": "Human Readable Name",
  "description": "When should this skill be loaded? Describe the user intent that should trigger it. Be specific.",
  "rationale": "Why does this skill help? What problem does it solve?",
  "content": "Full SKILL.md content including YAML frontmatter"
}

SKILL.md format:
\`\`\`
---
name: skill-name
description: Trigger description (same as 'description' field above)
---

# Overview
Brief description.

## When to use
List of situations.

## Steps / Guidelines
Concrete instructions for the agent.

## Examples
Optional examples of tool usage or patterns.
\`\`\`

Rules:
- Output ONLY valid JSON, no markdown, no explanation outside the JSON.
- If no skill is worth creating, output an empty array: []
- Maximum 3 proposals per analysis.
- Skills should be specific and actionable, not generic advice.
- The description field (trigger) is the MOST important — make it precise.
- If the analyzed traces already used an existing skill and the improvement belongs in that skill, return action="patch" for that exact skillId.
- Only return action="create" when a new skill is genuinely needed and no used skill should be updated.`

// ─────────────────────────────────────────────────────────
// Trace summarization helpers
// ─────────────────────────────────────────────────────────

/** Score how "interesting" a trace is for optimization purposes. */
function interestScore(trace: AgentTrace): number {
  let score = 0
  // More tool calls = more complex = more worth capturing
  score += Math.min(trace.totalToolCalls * 2, 20)
  // Retries / corrections often mean the agent had to learn something
  const allText = trace.steps.map((s) => s.assistantText).join(" ").toLowerCase()
  if (allText.includes("retry") || allText.includes("重试")) score += 5
  if (allText.includes("actually") || allText.includes("wait") || allText.includes("actually,")) score += 3
  if (allText.includes("let me reconsider") || allText.includes("重新考虑")) score += 5
  // Errors indicate a learning opportunity
  if (trace.outcome === "error") score += 3
  return score
}

/** Create a compact trace summary for the LLM (avoids huge token usage). */
function summarizeTrace(trace: AgentTrace): string {
  const toolList = trace.steps
    .flatMap((s) => s.toolCalls.map((tc) => tc.name))
    .join(", ")

  const stepSummaries = trace.steps
    .map((s) => {
      const tools = s.toolCalls.map((tc) => `${tc.name}(${JSON.stringify(tc.args).slice(0, 80)})`).join("; ")
      const text = s.assistantText.slice(0, 200).replace(/\n/g, " ")
      return `  Step ${s.index}: [${tools || "no tools"}] ${text}`
    })
    .join("\n")

  return `Trace ${trace.traceId.slice(0, 8)} | ${trace.outcome} | ${trace.totalToolCalls} tool calls
User message: ${trace.userMessage.slice(0, 150)}
Tools used: ${toolList}
Active skills: ${trace.activeSkills.join(", ") || "(none)"}
Steps:
${stepSummaries}`
}

// ─────────────────────────────────────────────────────────
// Existing skills reader
// ─────────────────────────────────────────────────────────

function readCustomSkillRecords(filterIds?: string[]): CustomSkillRecord[] {
  const dir = getCustomSkillsDir()
  if (!existsSync(dir)) return []
  const filter = filterIds ? new Set(filterIds) : null
  try {
    return readdirSync(dir, { withFileTypes: true })
      .filter((d) => d.isDirectory())
      .filter((d) => !filter || filter.has(d.name))
      .map((d) => {
        const mdPath = join(dir, d.name, "SKILL.md")
        if (!existsSync(mdPath)) return null
        return {
          skillId: d.name,
          content: readFileSync(mdPath, "utf-8")
        } satisfies CustomSkillRecord
      })
      .filter((skill): skill is CustomSkillRecord => !!skill)
  } catch {
    return []
  }
}

function formatSkillRecords(skills: CustomSkillRecord[], slice: number): string {
  if (skills.length === 0) return "(none)"
  return skills
    .map((skill) => `--- ${skill.skillId} ---\n${skill.content.slice(0, slice)}`)
    .join("\n\n")
}

function getUniqueActiveSkillIds(traces: AgentTrace[]): string[] {
  return Array.from(
    new Set(
      traces
        .flatMap((trace) => trace.activeSkills)
        .map((skillId) => skillId.trim())
        .filter(Boolean)
    )
  )
}

function extractJsonArrayCandidates(text: string): string[] {
  const candidates: string[] = []

  for (let start = text.indexOf("["); start !== -1; start = text.indexOf("[", start + 1)) {
    let depth = 0
    let inString = false
    let escaping = false

    for (let i = start; i < text.length; i++) {
      const ch = text[i]

      if (inString) {
        if (escaping) {
          escaping = false
          continue
        }
        if (ch === "\\") {
          escaping = true
          continue
        }
        if (ch === "\"") {
          inString = false
        }
        continue
      }

      if (ch === "\"") {
        inString = true
        continue
      }

      if (ch === "[") {
        depth += 1
        continue
      }

      if (ch === "]") {
        depth -= 1
        if (depth === 0) {
          candidates.push(text.slice(start, i + 1))
          break
        }
      }
    }
  }

  return candidates
}

