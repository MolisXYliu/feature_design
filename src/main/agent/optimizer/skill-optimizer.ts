/**
 * Skill Optimizer (Offline Optimization Loop)
 *
 * Inspired by GEPA (Gradient-free Evolution of Prompt Agents, ICLR 2026 Oral).
 *
 * Flow:
 *   1. Read recent execution traces from ~/.cmbcoworkagent/traces/
 *   2. Identify "interesting" sessions: high tool-call count, repeated patterns,
 *      or sessions where the agent retried or corrected itself.
 *   3. For each identified session, call an LLM to:
 *        a. Summarize what the agent did
 *        b. Suggest a skill (or patch an existing skill) that would make
 *           future runs faster / more reliable
 *   4. Return a list of SkillOptimizationCandidate objects — NOT applied yet.
 *   5. The caller (IPC handler / UI) presents candidates to the user for review.
 *   6. User approves/rejects each candidate; approved ones are written via
 *      the existing manage_skill infrastructure.
 */

import { ChatOpenAI } from "@langchain/openai"
import { HumanMessage, SystemMessage } from "@langchain/core/messages"
import { join } from "path"
import { existsSync, readdirSync, readFileSync } from "fs"
import { v4 as uuid } from "uuid"
import { getCustomSkillsDir } from "../../storage"
import { readRecentTraces, readThreadTraces } from "../trace/collector"
import type { AgentTrace } from "../trace/types"
import { stripLLMFormatting } from "../skill-evolution/skill-proposal-logic"

// ─────────────────────────────────────────────────────────
// Types
// ─────────────────────────────────────────────────────────

export type CandidateAction = "create" | "patch"

/**
 * A proposed skill improvement generated by the optimizer.
 * Must be reviewed and approved by the user before being applied.
 */
export interface SkillOptimizationCandidate {
  /** Unique ID for this candidate (used by the approval UI) */
  candidateId: string
  /** Whether to create a new skill or patch an existing one */
  action: CandidateAction
  /** Skill file name / directory (snake_case) */
  skillId: string
  /** Human-readable skill name */
  name: string
  /**
   * Trigger description — when should the agent load this skill?
   * This is the most important field; it controls skill injection.
   */
  description: string
  /** Full proposed SKILL.md content (including YAML frontmatter) */
  proposedContent: string
  /**
   * Reasoning: why did the optimizer suggest this?
   * Shown to the user in the review UI.
   */
  rationale: string
  /**
   * The trace IDs that motivated this candidate.
   * Shown in the review UI as "based on N sessions".
   */
  sourceTraceIds: string[]
  /** When this candidate was generated */
  generatedAt: string
  /** Approval status (set by UI) */
  status: "pending" | "approved" | "rejected"
}

/** Result of a single optimization run. */
export interface OptimizationRunResult {
  /** When the run started */
  startedAt: string
  /** When the run ended */
  endedAt: string
  /** How many traces were analyzed */
  tracesAnalyzed: number
  /** Generated candidates (may be empty if nothing interesting found) */
  candidates: SkillOptimizationCandidate[]
  /** Summary message from the optimizer */
  summary: string
}

interface CustomSkillRecord {
  skillId: string
  content: string
}

